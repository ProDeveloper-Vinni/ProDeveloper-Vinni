# list into string with space using join method 

def sample(my_list):
	string_1 = " "
	return string_1.join(my_list)
print(sample(['python','with','datascience']))

# list into string without space using join method

def sample(my_list):
	string_1 = ""
	return string_1.join(my_list)
print(sample(['python','with','datascience']))